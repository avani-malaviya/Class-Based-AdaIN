class Net(nn.Module):
    def __init__(self, adain, encoder, decoder):
        super(Net, self).__init__()
        enc_layers = list(encoder.children())
        self.enc_1 = nn.Sequential(*enc_layers[:4])  # input -> relu1_1
        self.enc_2 = nn.Sequential(*enc_layers[4:11])  # relu1_1 -> relu2_1
        self.enc_3 = nn.Sequential(*enc_layers[11:18])  # relu2_1 -> relu3_1
        self.enc_4 = nn.Sequential(*enc_layers[18:31])  # relu3_1 -> relu4_1
        self.decoder = decoder
        self.mse_loss = nn.MSELoss()
        self.adain = adain

        # fix the encoder
        for name in ['enc_1', 'enc_2', 'enc_3', 'enc_4']:
            for param in getattr(self, name).parameters():
                param.requires_grad = False

    # extract relu1_1, relu2_1, relu3_1, relu4_1 from input image
    def encode_with_intermediate(self, input):
        results = [input]
        for i in range(4):
            func = getattr(self, 'enc_{:d}'.format(i + 1))
            results.append(func(results[-1]))
        return results[1:]

    # extract relu4_1 from input image
    def encode(self, input):
        for i in range(4):
            input = getattr(self, 'enc_{:d}'.format(i + 1))(input)
        return input

    def calc_content_loss(self, input, target):
        assert (input.size() == target.size())
        assert (target.requires_grad is False)
        return self.mse_loss(input, target)

    def calc_style_loss_old(self, input, target, input_sem, target_sem):
        assert (input.size() == target.size())
        assert (target.requires_grad is False)
        
        total_loss = 0 
        for class_id in torch.unique(input_sem):
            input_mask = F.interpolate((input_sem == class_id).float(), size = input.shape[2:], mode = 'bilinear')
            target_mask = F.interpolate((target_sem == class_id).float(), size = target.shape[2:], mode = 'bilinear')

            masked_input = input*input_mask 
            masked_target = target*target_mask 

            input_mean, input_std = calc_mean_std(masked_input)
            target_mean, target_std = calc_mean_std(masked_target)

            total_loss += self.mse_loss(input_mean, target_mean) + self.mse_loss(input_std, target_std)

        return total_loss
    
    def calc_style_loss(self, input, target):
        assert (input.size() == target.size())
        assert (target.requires_grad is False)
        input_mean, input_std = calc_mean_std(input)
        target_mean, target_std = calc_mean_std(target)
        return self.mse_loss(input_mean, target_mean) + \
               self.mse_loss(input_std, target_std)
    
    def calc_laplacian_loss(self, output):
        batch_size = output.size(0)
        total_reg_loss = 0.0

        for i in range(batch_size):
            single_output = output[i]
            single_output = single_output/255.0
            output_np = np.array(single_output.detach().cpu().numpy(),dtype=np.float64)
            output_np_transposed = output_np.transpose()
            H, W, C = output_np_transposed.shape
            
            # Assuming lkm_laplacian returns a tuple where the second element is the laplacian diagonal
            laplacian_sm = cf_laplacian(output_np_transposed)

            # Step 1: Convert to COO format
            laplacian_coo = laplacian_sm.tocoo()

            # Step 2: Create PyTorch sparse tensor
            indices = torch.LongTensor(np.array([laplacian_coo.row, laplacian_coo.col]))
            values = torch.FloatTensor(np.array(laplacian_coo.data))
            shape = laplacian_coo.shape

            laplacian_torch = torch.sparse_coo_tensor(indices, values, shape).to(output.device)
            
            # Convert output_np_transposed to a torch tensor with float32 dtype
            output_torch = torch.tensor(output_np_transposed, dtype=torch.float32, device=output.device) 
            
            # Flatten the output tensor
            output_flat = output_torch.reshape(H*W, C)
            
            # Calculate the loss using diagonal tensor directly
            reg_loss = torch.sparse.mm(laplacian_torch,output_flat)
            reg_loss = torch.sum(torch.mm(torch.transpose(output_flat,0,1),reg_loss))
            total_reg_loss += reg_loss.item()

        avg_reg_loss = total_reg_loss / batch_size
        avg_reg_loss_tensor = torch.tensor(avg_reg_loss, dtype=output.dtype, device=output.device)
        
        return avg_reg_loss_tensor


    def forward(self, content, style, content_sem, style_sem, alpha=1.0):
        assert 0 <= alpha <= 1
        style_feats = self.encode_with_intermediate(style)
        content_feat = self.encode(content)
        t = self.adain(content_feat, style_feats[-1], content_sem, style_sem)
        t = alpha * t + (1 - alpha) * content_feat

        g_t = self.decoder(t)
        g_t_feats = self.encode_with_intermediate(g_t)

        loss_c = self.calc_content_loss(g_t_feats[-1], t)
        loss_s = 0
        for i in range(4):
            loss_s += self.calc_style_loss(g_t_feats[i], style_feats[i])

#        loss_m = self.calc_laplacian_loss(g_t)
        
        return loss_c, loss_s #, loss_m